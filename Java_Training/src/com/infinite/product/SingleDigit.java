package com.infinite.product;

public class SingleDigit {

	public static void main(String[] args) {
		int a = 0;
		int b = 10;
		if(a>=0 && a<10)
		{
			System.out.println("a is Single Digit Number");
		}
		else
		{
			System.out.println(" a is Not a Single Digit Number");
		}
		if(b>0 && b<10)
		{
			System.out.println("b is Single Digit Number");
		}
		else
		{
			System.out.println("b is Not a Single Digit Number");
		}

	}

}
